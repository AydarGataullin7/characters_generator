OUTPUT_FOLDER = "results"
TEMPLATE_FOLDER = "temp/charsheet.svg"


def convert_skills_to_runic(skills_list, mapping):
    runic_skills = []
    for skill in skills_list:
        converted_skill = skill
        for rus_letter, runic_letter in mapping.items():
            converted_skill = converted_skill.replace(rus_letter, runic_letter)
        runic_skills.append(converted_skill)
    return runic_skills


def generate_character_context(fake, runic_skills):
    unique_skills = random.sample(runic_skills, 3)
    return {
        "first_name": fake.first_name(),
        "last_name": fake.last_name(),
        "job": fake.job(),
        "town": fake.city(),
        "strength": random.randint(3, 18),
        "agility": random.randint(3, 18),
        "endurance": random.randint(3, 18),
        "intelligence": random.randint(3, 18),
        "luck": random.randint(3, 18),
        "skill_1": unique_skills[0],
        "skill_2": unique_skills[1],
        "skill_3": unique_skills[2]
    }


def main():
    runic_skills = convert_skills_to_runic(SKILLS, LETTERS_MAPPING)

    os.makedirs(OUTPUT_FOLDER, exist_ok=True)

    fake = Faker("ru_RU")

    for i in range(10):
        context = generate_character_context(fake, runic_skills)

        filename = os.path.join(OUTPUT_FOLDER, "character_{}.svg".format(i+1))
        file_operations.render_template(TEMPLATE_FOLDER, filename, context)


if __name__ == '__main__':
    main()
